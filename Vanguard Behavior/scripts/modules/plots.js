    static canEditBlock(player, blockLoc, action = "place_gen") {
        const obj = world.scoreboard.getObjective("isDayTime");
        let isDay = 0;
        if (obj) {
            try { isDay = obj.getScore("isDayTime"); } catch { }
        }

        const isNight = isDay !== 1;

        let insidePlotKey = null;
        let insidePlotConfig = null;

        for (const plotKey in plots) {
            const plotConfig = plots[plotKey];
            if (this.isPointInside(blockLoc, plotConfig.plotCords.from, plotConfig.plotCords.to)) {
                insidePlotKey = plotKey;
                insidePlotConfig = plotConfig;
                break;
            }
        }

        if (!insidePlotKey) return true;

        let owningPlotId = null;
        let owningPlot = null;

        for (const plotId of activeplots) {
            const plot = this.getPlot(plotId);
            if (plot && plot.claimed_plot === insidePlotKey) {
                owningPlotId = plotId;
                owningPlot = plot;
                break;
            }
        }

        if (!owningPlot) {
            if (isNight) return true;
            return false;
        }

        if (isNight) return true;

        if (owningPlot.owner_id === player.id) return true;

        const teammate = owningPlot.teammates?.find(t => t.player_id === player.id);
        if (!teammate) return false;

        switch (action) {
            case "canBreak":
                return teammate.perms.canBreak === true;
            case "canPlace":
                return teammate.perms.canPlace === true;
            case "canInteract":
                return teammate.perms.canInteract === true;
            case "place_gen":
                return teammate.perms.place_gen === true;
            case "place_miner":
                return teammate.perms.place_miner === true;
            case "pickup_miner":
                return teammate.perms.pickup_miner === true;
            case "pickup_gen":
                return teammate.perms.pickup_gen === true;
            default:
                return teammate.perms[action] === true;
        }
    }


    static isPointInside(loc, from, to) {
        return loc.x >= Math.min(from.x, to.x) && loc.x <= Math.max(from.x, to.x) &&
            loc.y >= Math.min(from.y, to.y) && loc.y <= Math.max(from.y, to.y) &&
            loc.z >= Math.min(from.z, to.z) && loc.z <= Math.max(from.z, to.z);
    }

    static minMax(a, b) {
        return [Math.min(a, b), Math.max(a, b)];
    }

    static fillAreaAPI(dim, from, to, block, parts = 6) {
        const minX = Math.min(from.x, to.x);
        const maxX = Math.max(from.x, to.x);
        const minY = Math.max(Math.min(from.y, to.y), -64);
        const maxY = Math.min(Math.max(from.y, to.y), 300);
        const minZ = Math.min(from.z, to.z);
        const maxZ = Math.max(from.z, to.z);
        const totalWidth = maxX - minX + 1;

        for (let i = 0; i < parts; i++) {
            const startX = minX + Math.ceil((totalWidth / parts) * i);
            let endX = minX + Math.ceil((totalWidth / parts) * (i + 1)) - 1;
            if (i === parts - 1 || endX > maxX) endX = maxX;
            if (startX > maxX) break;

            try {
                dim.fillBlocks(
                    new BlockVolume(
                        { x: startX, y: minY, z: minZ },
                        { x: endX, y: maxY, z: maxZ }
                    ),
                    block
                );
            } catch { }
        }
    }

    static setGrassFloorAPI(dim, from, to) {
        const [minX, maxX] = this.minMax(from.x, to.x);
        const [minZ, maxZ] = this.minMax(from.z, to.z);
        const y = Math.min(from.y, to.y);
        try {
            dim.fillBlocks(
                new BlockVolume({ x: minX, y, z: minZ }, { x: maxX, y, z: maxZ }),
                "minecraft:grass_block"
            );
        } catch { }
    }

