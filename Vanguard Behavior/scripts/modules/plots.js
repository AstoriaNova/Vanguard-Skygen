world.beforeEvents.entityHurt.subscribe((event) => {
    const { hurtEntity, damageSource } = event;
    if (damageSource.cause !== EntityDamageCause.entityAttack) return;

    const attacker = damageSource.damagingEntity;
    if (!attacker || attacker.typeId !== "minecraft:player") return;
    if (hurtEntity.typeId !== "minecraft:player") return;

    const attackerMyPlot = Plots.getMyPlot(attacker);
    const attackerTeamPlot = Plots.getTeammatePlot(attacker);
    const victimMyPlot = Plots.getMyPlot(hurtEntity);
    const victimTeamPlot = Plots.getTeammatePlot(hurtEntity);

    let samePlot = false;
    let plot = null;

    if (attackerMyPlot && (attackerMyPlot === victimMyPlot || attackerMyPlot === victimTeamPlot)) {
        samePlot = true;
        plot = Plots.getPlot(attackerMyPlot);
    } else if (attackerTeamPlot && (attackerTeamPlot === victimMyPlot || attackerTeamPlot === victimTeamPlot)) {
        samePlot = true;
        plot = Plots.getPlot(attackerTeamPlot);
    }

    if (samePlot && plot && !plot.team_killing) {
        event.cancel = true;
    }
});
system.runInterval(() => {
    const obj = world.scoreboard.getObjective("isDayTime");
    let isDay = 0;
    if (obj) {
        try { isDay = obj.getScore("isDayTime"); } catch { }
    }

    const dim = world.getDimension("overworld");
    const buffer = 1;

    for (const player of dim.getPlayers()) {
        const loc = player.location;
        let insideAnyPlot = false;
        let insideOwnedPlot = false;
        let teleportPlot = null;
        let isUnclaimed = false;

        const myPlotId = Plots.getMyPlot(player);
        const teamPlotId = Plots.getTeammatePlot(player);

        if (player.getGameMode() === GameMode.Creative || player.getGameMode() === GameMode.Spectator) {
            if (player.hasTag("KOS")) player.removeTag("KOS");
            continue;
        }

        for (const plotKey in plots) {
            const plotConfig = plots[plotKey];

            const insideMain =
                loc.x >= Math.min(plotConfig.plotCords.from.x, plotConfig.plotCords.to.x) - buffer &&
                loc.x <= Math.max(plotConfig.plotCords.from.x, plotConfig.plotCords.to.x) + buffer &&
                loc.y >= Math.min(plotConfig.plotCords.from.y, plotConfig.plotCords.to.y) - buffer &&
                loc.y <= Math.max(plotConfig.plotCords.from.y, plotConfig.plotCords.to.y) + buffer &&
                loc.z >= Math.min(plotConfig.plotCords.from.z, plotConfig.plotCords.to.z) - buffer &&
                loc.z <= Math.max(plotConfig.plotCords.from.z, plotConfig.plotCords.to.z) + buffer;

            const insideClose =
                loc.x >= Math.min(plotConfig.closePlotCords.from.x, plotConfig.closePlotCords.to.x) - buffer &&
                loc.x <= Math.max(plotConfig.closePlotCords.from.x, plotConfig.closePlotCords.to.x) + buffer &&
                loc.y >= Math.min(plotConfig.closePlotCords.from.y, plotConfig.closePlotCords.to.y) - buffer &&
                loc.y <= Math.max(plotConfig.closePlotCords.from.y, plotConfig.closePlotCords.to.y) + buffer &&
                loc.z >= Math.min(plotConfig.closePlotCords.from.z, plotConfig.closePlotCords.to.z) - buffer &&
                loc.z <= Math.max(plotConfig.closePlotCords.from.z, plotConfig.closePlotCords.to.z) + buffer;

            if (insideMain || insideClose) {
                insideAnyPlot = true;
                teleportPlot = plotConfig;

                let isClaimed = false;
                for (const plotId of activeplots) {
                    const plot = Plots.getPlot(plotId);
                    if (plot && plot.claimed_plot === plotKey) {
                        isClaimed = true;
                        if (plotId === myPlotId || plotId === teamPlotId) {
                            insideOwnedPlot = true;
                        }
                        break;
                    }
                }

                if (!isClaimed) {
                    isUnclaimed = true;
                }
                break;
            }
        }

        if (isDay === 1) {
            if (insideAnyPlot && !insideOwnedPlot) {
                player.teleport(teleportPlot?.entrance || { x: 0, y: 100, z: 0 });

                if (isUnclaimed) {
                    player.sendError("You cannot access unclaimed plots.");
                } else {
                    player.sendError("You can only enter during Raid Night.");
                }
                player.playSound("random.break");
            }
            if (player.hasTag("KOS")) player.removeTag("KOS");
        } else {
            if (insideAnyPlot) {
                if (!player.hasTag("KOS")) player.addTag("KOS");
            } else {
                if (player.hasTag("KOS")) player.removeTag("KOS");
            }
        }
    }
}, 5);

